package com.loiane.cursojava.aula17.labs;

public class Ex06 {

    public static void main(String[] args) {                

        // Exibe os números um abaixo do outro
        for (int i = 1; i <= 20; i++) {
            System.out.println(i);
        }

        // Exibe os números um ao lado do outro
        for (int i = 1; i <= 20; i++) {
            System.out.print(i + " ");
        }

        System.out.println();
    }
}
